package com.example.filemanager.pojo;

public class solved {
    public String chunk;
    public String solve;

    public solved(String chunk, String solve) {
        this.chunk = chunk;
        this.solve = solve;
    }

    public String getChunk() {
        return chunk;
    }

    public String getSolve() {
        return solve;
    }

    public void setChunk(String chunk) {
        this.chunk = chunk;
    }

    public void setSolve(String solve) {
        this.solve = solve;
    }

}
